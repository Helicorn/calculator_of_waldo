-- Oracle 참조 DDL
--   Spring Boot 프로필 `oracle` + `spring.jpa.hibernate.ddl-auto=none` 일 때, 스키마를 수동으로 맞출 때 사용합니다.
--   설정은 `backend/src/main/resources/application-oracle.yml` 참고 (데이터소스 URL/계정, `hibernate.default_schema` 등).
-- 실행 위치
--   접속한 사용자 스키마에서 그대로 실행하거나, 다른 스키마면 객체 소유자/권한(GRANT)을 사전에 맞추세요.
--   `hibernate.default_schema`는 환경 변수 `ORACLE_SCHEMA`가 있으면 그 값, 없으면 datasource username과 일치해야 합니다.
-- JPA 엔티티와의 대응
--   t_user              ← com.waldo.TUser
--   t_pokemon_move      ← com.waldo.user.data.pokemon.PokemonMoveEntity
--   t_pokemon_ability     ← com.waldo.games.pokemon.PokemonAbilityEntity
-- 애플리케이션 외부(예: Swing `program`)에서 쓰는 테이블 — 엔티티 매핑 없음, 동일 스키마에 함께 두면 됩니다.
--   LOL_SEED_PLAYER, LOL_MATCH, LOL_MATCH_PARTICIPANT, LOL_CHAMPION_STATS

-- 시퀀스: TUser @SequenceGenerator(sequenceName = "seq_t_user_no")
CREATE SEQUENCE seq_t_user_no
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

CREATE TABLE t_user (
    no              NUMBER          NOT NULL,
    account         VARCHAR2(30),
    name            VARCHAR2(30),
    password        VARCHAR2(4000),
    phone           VARCHAR2(30),
    delyn           CHAR(1)         NOT NULL,
    username        VARCHAR2(30),
    authority       VARCHAR2(30),
    reg_dt          TIMESTAMP       NOT NULL,
    upd_dt          TIMESTAMP,
    last_login_dt   TIMESTAMP,
    CONSTRAINT pk_t_user PRIMARY KEY (no)
);

CREATE TABLE t_pokemon_move (
    move_id          NUMBER          NOT NULL,
    move_name_en     VARCHAR2(100)   NOT NULL,
    move_name_ko     VARCHAR2(200),
    type_name        VARCHAR2(30)    NOT NULL,
    damage_class     VARCHAR2(20)    NOT NULL,
    power            NUMBER,
    accuracy         NUMBER,
    pp               NUMBER,
    priority         NUMBER,
    target_name      VARCHAR2(50),
    short_effect_ko  VARCHAR2(1000),
    effect_chance    NUMBER,
    created_at       TIMESTAMP,
    updated_at       TIMESTAMP,
    CONSTRAINT pk_t_pokemon_move PRIMARY KEY (move_id)
);

CREATE TABLE t_pokemon_ability (
    ability_id           NUMBER        NOT NULL,
    ability_name_en      VARCHAR2(100),
    effect_en            CLOB,
    ability_name_ko      VARCHAR2(200),
    effect_ko            CLOB,
    translation_status   VARCHAR2(20),
    is_manual_override   CHAR(1),
    last_synced_at       TIMESTAMP,
    updated_at           TIMESTAMP,
    CONSTRAINT pk_t_pokemon_ability PRIMARY KEY (ability_id)
);

CREATE TABLE LOL_SEED_PLAYER (
    PLAYER_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PUUID            VARCHAR2(100) NOT NULL,
    TIER             VARCHAR2(20) NOT NULL,
    RANK_DIVISION    VARCHAR2(10) NOT NULL,
    QUEUE_TYPE       VARCHAR2(30) NOT NULL,
    IS_ACTIVE        CHAR(1) DEFAULT 'Y' NOT NULL,
    CREATED_AT       TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT       TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT UK_LOL_SEED_PLAYER_01 UNIQUE (PUUID), -- puuid 중복되지 않는 제약조건
    CONSTRAINT CK_LOL_SEED_PLAYER_01 CHECK (IS_ACTIVE IN ('Y', 'N')) -- Y,N만 들어가게 하는 제약조건
);

CREATE INDEX IDX_LOL_SEED_PLAYER_01 ON LOL_SEED_PLAYER (TIER, RANK_DIVISION, QUEUE_TYPE); 
CREATE INDEX IDX_LOL_SEED_PLAYER_02 ON LOL_SEED_PLAYER (IS_ACTIVE);

CREATE TABLE LOL_MATCH (
    MATCH_ID         VARCHAR2(30) NOT NULL,
    QUEUE_ID         NUMBER NOT NULL,
    PATCH_VERSION    VARCHAR2(10) NOT NULL,
    GAME_START_TIME  NUMBER,
    CREATED_AT       TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT PK_LOL_MATCH PRIMARY KEY (MATCH_ID)
);

COMMENT ON TABLE LOL_MATCH IS '매치 기본 정보 테이블';

COMMENT ON COLUMN LOL_MATCH.MATCH_ID IS '라이엇 매치 고유 ID';
COMMENT ON COLUMN LOL_MATCH.QUEUE_ID IS '큐 ID (예: 420=솔로랭크)';
COMMENT ON COLUMN LOL_MATCH.PATCH_VERSION IS '패치 버전 (예: 14.7)';
COMMENT ON COLUMN LOL_MATCH.GAME_START_TIME IS '게임 시작 시각(epoch milliseconds)';
COMMENT ON COLUMN LOL_MATCH.CREATED_AT IS '데이터 생성 시각';

CREATE TABLE LOL_MATCH_PARTICIPANT (
    MATCH_PARTICIPANT_ID  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MATCH_ID              VARCHAR2(30) NOT NULL,
    PUUID                 VARCHAR2(100) NOT NULL,
    CHAMPION_ID           NUMBER NOT NULL,
    LINE                 VARCHAR2(20) NOT NULL,
    WIN_YN                CHAR(1) NOT NULL,
    KILLS                 NUMBER DEFAULT 0 NOT NULL,
    DEATHS                NUMBER DEFAULT 0 NOT NULL,
    ASSISTS               NUMBER DEFAULT 0 NOT NULL,
    CREATED_AT            TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT FK_LOL_MATCH_PARTICIPANT_01
        FOREIGN KEY (MATCH_ID) REFERENCES LOL_MATCH (MATCH_ID),

    CONSTRAINT UK_LOL_MATCH_PARTICIPANT_01
        UNIQUE (MATCH_ID, PUUID),

    CONSTRAINT CK_LOL_MATCH_PARTICIPANT_01
        CHECK (WIN_YN IN ('Y', 'N'))
);

COMMENT ON TABLE LOL_MATCH_PARTICIPANT IS '매치 참가자 정보 테이블';

COMMENT ON COLUMN LOL_MATCH_PARTICIPANT.MATCH_PARTICIPANT_ID IS '매치 참가자 테이블 내부 PK';
COMMENT ON COLUMN LOL_MATCH_PARTICIPANT.MATCH_ID IS '라이엇 매치 고유 ID';
COMMENT ON COLUMN LOL_MATCH_PARTICIPANT.PUUID IS '플레이어 고유 식별자';
COMMENT ON COLUMN LOL_MATCH_PARTICIPANT.CHAMPION_ID IS '플레이한 챔피언 ID';
COMMENT ON COLUMN LOL_MATCH_PARTICIPANT.LINE IS '라인 정보';
COMMENT ON COLUMN LOL_MATCH_PARTICIPANT.WIN_YN IS '승리 여부 (Y/N)';
COMMENT ON COLUMN LOL_MATCH_PARTICIPANT.KILLS IS '킬 수';
COMMENT ON COLUMN LOL_MATCH_PARTICIPANT.DEATHS IS '데스 수';
COMMENT ON COLUMN LOL_MATCH_PARTICIPANT.ASSISTS IS '어시스트 수';
COMMENT ON COLUMN LOL_MATCH_PARTICIPANT.CREATED_AT IS '데이터 생성 시각';

CREATE INDEX IDX_LOL_MATCH_PARTICIPANT_01
    ON LOL_MATCH_PARTICIPANT (MATCH_ID);

CREATE INDEX IDX_LOL_MATCH_PARTICIPANT_02
    ON LOL_MATCH_PARTICIPANT (CHAMPION_ID);

CREATE INDEX IDX_LOL_MATCH_PARTICIPANT_03
    ON LOL_MATCH_PARTICIPANT (LINE);

CREATE INDEX IDX_LOL_MATCH_PARTICIPANT_04
    ON LOL_MATCH_PARTICIPANT (MATCH_ID, CHAMPION_ID);

CREATE TABLE LOL_CHAMPION_STATS (
    STATS_ID         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PATCH_VERSION    VARCHAR2(10) NOT NULL,
    QUEUE_ID         NUMBER NOT NULL,
    CHAMPION_ID      NUMBER NOT NULL,
    LINE             VARCHAR2(20) NOT NULL,
    GAMES            NUMBER DEFAULT 0 NOT NULL,
    WINS             NUMBER DEFAULT 0 NOT NULL,
    LOSSES           NUMBER DEFAULT 0 NOT NULL,
    WIN_RATE         NUMBER(5,2) NOT NULL,
    PICK_RATE        NUMBER(5,2) NOT NULL,
    CREATED_AT       TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT       TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT UK_LOL_CHAMPION_STATS_01
        UNIQUE (PATCH_VERSION, QUEUE_ID, CHAMPION_ID, LINE)
);

COMMENT ON TABLE LOL_CHAMPION_STATS IS '패치, 큐, 라인 기준 챔피언 통계 테이블';

COMMENT ON COLUMN LOL_CHAMPION_STATS.STATS_ID IS '챔피언 통계 테이블 내부 PK';
COMMENT ON COLUMN LOL_CHAMPION_STATS.PATCH_VERSION IS '패치 버전';
COMMENT ON COLUMN LOL_CHAMPION_STATS.QUEUE_ID IS '큐 ID';
COMMENT ON COLUMN LOL_CHAMPION_STATS.CHAMPION_ID IS '챔피언 ID';
COMMENT ON COLUMN LOL_CHAMPION_STATS.LINE IS '라인 정보';
COMMENT ON COLUMN LOL_CHAMPION_STATS.GAMES IS '해당 라인에서 플레이된 게임 수';
COMMENT ON COLUMN LOL_CHAMPION_STATS.WINS IS '승리 수';
COMMENT ON COLUMN LOL_CHAMPION_STATS.LOSSES IS '패배 수';
COMMENT ON COLUMN LOL_CHAMPION_STATS.WIN_RATE IS '승률';
COMMENT ON COLUMN LOL_CHAMPION_STATS.PICK_RATE IS '라인 기준 픽률';
COMMENT ON COLUMN LOL_CHAMPION_STATS.CREATED_AT IS '데이터 생성 시각';
COMMENT ON COLUMN LOL_CHAMPION_STATS.UPDATED_AT IS '데이터 수정 시각';