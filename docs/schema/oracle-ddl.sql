-- Oracle 참조 DDL
--   Spring Boot 프로필 `oracle` + `spring.jpa.hibernate.ddl-auto=none` 일 때, 스키마를 수동으로 맞출 때 사용합니다.
--   설정은 `backend/src/main/resources/application-oracle.yml` 참고 (데이터소스 URL/계정, `hibernate.default_schema` 등).
-- 실행 위치
--   접속한 사용자 스키마에서 그대로 실행하거나, 다른 스키마면 객체 소유자/권한(GRANT)을 사전에 맞추세요.
--   `hibernate.default_schema`는 환경 변수 `ORACLE_SCHEMA`가 있으면 그 값, 없으면 datasource username과 일치해야 합니다.
-- JPA 엔티티와의 대응
--   t_user              ← com.waldo.TUser
--   t_pokemon_move      ← com.waldo.user.data.pokemon.PokemonMoveEntity
--   t_pokemon_ability     ← com.waldo.games.pokemon.PokemonAbilityEntity
-- 애플리케이션 외부(예: Swing `program`)에서 쓰는 테이블 — 엔티티 매핑 없음, 동일 스키마에 함께 두면 됩니다.
--   LOL_SEED_PLAYER, LOL_MATCH, LOL_MATCH_PARTICIPANT, LOL_CHAMPION_STATS
-- LoL 자유게시판 JPA 엔티티:
--   LOL_BOARD_HEAD      ← com.waldo.games.lol.board.LolBoardHeadEntity
--   LOL_BOARD           ← com.waldo.games.lol.board.LolBoardEntity
--   LOL_BOARD_COMMENT   ← com.waldo.games.lol.board.LolBoardCommentEntity
-- Overwatch 자유게시판 JPA 엔티티:
--   OVERWATCH_BOARD_HEAD      ← com.waldo.games.overwatch.board.OverwatchBoardHeadEntity
--   OVERWATCH_BOARD           ← com.waldo.games.overwatch.board.OverwatchBoardEntity
--   OVERWATCH_BOARD_COMMENT   ← com.waldo.games.overwatch.board.OverwatchBoardCommentEntity

-- 시퀀스: TUser @SequenceGenerator(sequenceName = "seq_t_user_no")
CREATE SEQUENCE seq_t_user_no
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

CREATE TABLE t_user (
    no              NUMBER          NOT NULL,
    account         VARCHAR2(30),
    name            VARCHAR2(30),
    password        VARCHAR2(4000),
    phone           VARCHAR2(30),
    delyn           CHAR(1)         NOT NULL,
    username        VARCHAR2(30),
    authority       VARCHAR2(30),
    reg_dt          TIMESTAMP       NOT NULL,
    upd_dt          TIMESTAMP,
    last_login_dt   TIMESTAMP,
    CONSTRAINT pk_t_user PRIMARY KEY (no)
);

