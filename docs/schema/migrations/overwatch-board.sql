CREATE TABLE OVERWATCH_BOARD (
    BOARD_ID      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_NO       NUMBER NOT NULL,
    TITLE         VARCHAR2(200) NOT NULL,
    CONTENT       CLOB NOT NULL,
    VIEW_CNT      NUMBER DEFAULT 0 NOT NULL,
    COMMENT_CNT   NUMBER DEFAULT 0 NOT NULL,
    NOTICE_YN     CHAR(1) DEFAULT 'N' NOT NULL,
    HEAD_ID       NUMBER,
    DEL_YN        CHAR(1) DEFAULT 'N' NOT NULL,
    CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT    TIMESTAMP,

    CONSTRAINT FK_OVERWATCH_BOARD_USER
        FOREIGN KEY (USER_NO) REFERENCES T_USER (NO),

    CONSTRAINT FK_OVERWATCH_BOARD_HEAD
        FOREIGN KEY (HEAD_ID) REFERENCES OVERWATCH_BOARD_HEAD (HEAD_ID),

    CONSTRAINT CK_OVERWATCH_BOARD_NOTICE_YN
        CHECK (NOTICE_YN IN ('Y', 'N')),

    CONSTRAINT CK_OVERWATCH_BOARD_DEL_YN
        CHECK (DEL_YN IN ('Y', 'N'))
);

CREATE INDEX IDX_OVERWATCH_BOARD_01 ON OVERWATCH_BOARD (DEL_YN, CREATED_AT DESC);
CREATE INDEX IDX_OVERWATCH_BOARD_02 ON OVERWATCH_BOARD (USER_NO);
CREATE INDEX IDX_OVERWATCH_BOARD_HEAD_FK ON OVERWATCH_BOARD (HEAD_ID);

CREATE TABLE OVERWATCH_BOARD_COMMENT (
    COMMENT_ID         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BOARD_ID           NUMBER NOT NULL,
    USER_NO            NUMBER NOT NULL,
    PARENT_COMMENT_ID  NUMBER,
    CONTENT            VARCHAR2(2000) NOT NULL,
    DEL_YN             CHAR(1) DEFAULT 'N' NOT NULL,
    CREATED_AT         TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT         TIMESTAMP,

    CONSTRAINT FK_OVERWATCH_BOARD_COMMENT_BOARD
        FOREIGN KEY (BOARD_ID) REFERENCES OVERWATCH_BOARD (BOARD_ID),

    CONSTRAINT FK_OVERWATCH_BOARD_COMMENT_USER
        FOREIGN KEY (USER_NO) REFERENCES T_USER (NO),

    CONSTRAINT FK_OVERWATCH_BOARD_COMMENT_PARENT
        FOREIGN KEY (PARENT_COMMENT_ID) REFERENCES OVERWATCH_BOARD_COMMENT (COMMENT_ID),

    CONSTRAINT CK_OVERWATCH_BOARD_COMMENT_DEL_YN
        CHECK (DEL_YN IN ('Y', 'N'))
);

CREATE INDEX IDX_OVERWATCH_BOARD_COMMENT_01
    ON OVERWATCH_BOARD_COMMENT (BOARD_ID, DEL_YN, CREATED_AT);

CREATE INDEX IDX_OVERWATCH_BOARD_COMMENT_02
    ON OVERWATCH_BOARD_COMMENT (PARENT_COMMENT_ID);
