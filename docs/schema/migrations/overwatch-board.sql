CREATE TABLE OVERWATCH_BOARD_HEAD (
    HEAD_ID     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LABEL       VARCHAR2(50) NOT NULL,
    SORT_ORDER  NUMBER DEFAULT 0 NOT NULL,

    CONSTRAINT UK_OVERWATCH_BOARD_HEAD_LABEL UNIQUE (LABEL)
);

